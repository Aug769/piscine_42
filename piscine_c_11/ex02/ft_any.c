int		ft_any(char **tab, int (*f)(char*))
{
	int		j;
	int		mod;

	j = 0;
	while (tab[j])
	{
		mod = (*f)(tab[j]);
		if (mod != 0)
			return (1);
		j++;
	}
	return (0);
}
